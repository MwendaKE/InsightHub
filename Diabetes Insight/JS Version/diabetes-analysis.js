async function loadDiabetesData(filePath) {
    return new Promise((resolve, reject) => {
        const results = [];
        fs.createReadStream(filePath)
            .pipe(csv())
            .on('data', (data) => results.push(data))
            .on('end', () => {
                console.log(`âœ… Diabetes data loaded: ${results.length} records`);
                const processedData = results.map(row => {
                    const processed = {};
                    for (const key in row) {
                        processed[key] = isNaN(row[key]) ? row[key] : parseFloat(row[key]);
                    }
                    return processed;
                });
                resolve(processedData);
            })
            .on('error', reject);
    });
}

function cleanDiabetesData(data) {
    const cleanedData = data.filter(row => 
        row.Glucose && row.BMI && row.Age && row.Outcome !== null
    );
    console.log(`âœ… Data cleaned: ${cleanedData.length} records remaining`);
    return cleanedData;
}

